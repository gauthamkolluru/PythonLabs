# Complete the countApplesAndOranges function below.
def countApplesAndOranges(s, t, a, b, apples, oranges):
    count_a = 0
    count_o = 0
    for ap in apples:
        if ap+a >= s and ap+a <= t:
            count_a += 1
    for ora in oranges:
        if ora+b >= s and ora+b <= t:
            count_o += 1
    print(count_a)
    print(count_o)


